class Style():
    def __init__(self):
            self.BLACK = '' 
            self.RED = ''
            self.GREEN = '' 
            self.YELLOW = ''         
            self.BLUE = ''  
            self.MAGENTA = ''  
            self.CYAN = ''  
            self.WHITE = ''
            self.UNDERLINE = ''  
            self.RESET= ''


    def set_black(self):
        print(self.BLACK)
    def set_red(self):
        print(self.RED)
    def set_green(self):
        print(self.GREEN)
    def set_yellow(self):
        print(self.YELLOW)
    def set_blue(self):
        print(self.BLUE)
    def set_magenta(self):
        print(self.MAGENTA)
    def set_underline(self):
        print(self.UNDERLINE)
    def reset(self):
        print(self.RESET)
